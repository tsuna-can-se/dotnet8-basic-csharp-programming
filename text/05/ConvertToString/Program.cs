// int -> string の変換
int intValue = 100;
string intString = intValue.ToString();
Console.WriteLine(intString);

// decimal -> string の変換
decimal decimalValue = 2000.50m;
string decimalString = decimalValue.ToString();
Console.WriteLine(decimalString);

// double -> string の変換
double doubleValue = 2000.50d;
string doubleString = doubleValue.ToString();
Console.WriteLine(doubleString);